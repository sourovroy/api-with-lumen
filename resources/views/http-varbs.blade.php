<h1>Allowed HTTP verbs</h1>

<p>You can use these HTTP methods.</p>

<table class="table">
	<tbody>
		<tr>
			<td>GET</td>
			<td>Get a resource or list of resources</td>
		</tr>
		<tr>
			<td>POST</td>
			<td>To create a resource</td>
		</tr>
		<tr>
			<td>PUT</td>
			<td>Update a resource</td>
		</tr>
		<tr>
			<td>DELETE</td>
			<td>To delete a resource</td>
		</tr>
	</tbody>
</table>
